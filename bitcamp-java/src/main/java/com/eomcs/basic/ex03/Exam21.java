package com.eomcs.basic.ex03;

// 리터럴 (literal)
// - 자바 언어에서 값을 표기하는 문법!!
public class Exam21 {
  public static void main(String[] args) {
    // 10진수 리터럴
    System.out.println(100); // 10진수 표기법

    //8진수 리터럴
    System.out.println(0144);

    //2진수
    System.out.println(0b01100100);
    System.out.println(0B01100100);

    //16진수
    System.out.println(0x0064);
    System.out.println(0X0064);

  }
}
